package org.example.flujocontrol;

public class switchcase {
    public static void main(String[] arg) {
        int numero =  3;

        switch(numero) {
            case 1:
                System.out.println("El numero es" + " " + numero);
                break;
            default:
                System.out.println("El numero ingresado no es un numero valido!");
        }
    }

}
